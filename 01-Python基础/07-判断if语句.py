# 案例三:if,else的使用
# 注意:默认输入的是str类型,而字符串类型不能直接判断,需要通过int转换一下
age = int(input("请输入年龄:"))
if age >= 18:
    print ("开始还房贷")
else:
    print ("好好学习,以后还房贷")
print ("一定会执行的代码")

