# if语句的基础语法:
# if 要判断的条件:
# (四个空格)条件成立时,要做的事情

# 案例一:判断是否满足18,18就可进网吧
age = 18
if age >= 18:
    print ("你已经成年了,该还房贷了")
    print ("可以把if中缩进部分看做完整的代码块")
print ("这里是一定会执行的,可以看做java的finally")
